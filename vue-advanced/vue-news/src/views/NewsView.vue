<template>
  <div >
    <p v-for="user in users" :key="user.id">
          {{user}}
    </p>
  </div>
</template>

<script>
import axios from 'axios';

export default {
  data() {
    return {
      users : [],
    }
  },
  created() {
    var vm = this;
    axios.get('https://api.hnpwa.com/v0/news/1.json')
      .then(function(response) {
        vm.users = response.data
      })
      .catch(function(error){
        console.log(error)
      })
  }
}
</script>

